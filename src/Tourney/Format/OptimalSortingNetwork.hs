-- | Choose the optimal sorting network for the number of players in the
-- tournament
optimalSortingNetwork :: Steps () ()
optimalSortingNetwork = do
  count <- getPlayerCount
  steps (networks ^?! ix count)

{-# NOINLINE networks #-}
networks :: IntMap (Steps () ())
networks =
  IntMap.fromList
    [ ( asInt maxSlot + 1
      , mapM_ (swaps . round_) matches
      )
    | matches :: [[Match]] <- map (map (map (\(a, b) -> Match (Slot a) (Slot b)))) networksRaw
    , let !(Just maxSlot) = maximumOf (each . each . likelyLoser) matches
    ]
  where
    networksRaw :: [[[(Int, Int)]]]
    networksRaw = case readMaybe (toString networksString) of
      Just nw -> nw
      Nothing -> error "Cannot parse networksString"

